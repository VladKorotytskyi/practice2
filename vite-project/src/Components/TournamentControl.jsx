import React from "react";
import { GameStatus } from "./GameStatus";
import { RoundTimer } from "./RoundTimer";
import { ScoreBoard } from "./ScoreBoard";

export class TournamentControl extends React.Component {
  state = {
    start: false,
  };

  startTour = () => this.setState({ start: true });
  stopTour = () => this.setState({ start: false });

  render() {
    return (
      <div>
        {this.state.start ? (
          <>
            <h2>Tournament is Live!</h2>
            <GameStatus />
            <RoundTimer />
            <ScoreBoard />
            <button onClick={this.stopTour}>End Tournament</button>
          </>
        ) : (
          <>
            <h2>Tournament not started</h2>
            <button onClick={this.startTour}>Start</button>
          </>
        )}
      </div>
    );
  }
}

